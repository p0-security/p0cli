import { createKeyPair, P0_KEY_FOLDER } from "../common/keys";
import yargs from "yargs";

export const sshKeyGenCommand = (yargs: yargs.Argv) =>
  yargs.command(
    "ssh-keygen",
    `Generate an SSH key pair for use with P0 and store it in your ${P0_KEY_FOLDER} folder, if one does not exist already. This key pair is also automatically generated by using any "p0 ssh/scp ..." command.`,
    (yargs) => yargs,
    async () => {
      await createKeyPair();
    }
  );
